"""this is the example of union method in the Sets"""


s1 = {10, 20, 30, "sandeep", "Srestaa"}
s2 = {40, 50, 30, 0, 20, "sandeep"}
print("\nthis is the example of using the union methods")
print(s1.union(s2))
print("\nthis is the example of using the intersection methods")
print(s1.intersection(s2))
print("\nthis is the example of using the difference methods")
print(s1.difference(s2))
print(s2.difference(s1))
